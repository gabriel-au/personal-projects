package br.com.martins.vendas.vo;

import java.util.HashMap;
import java.util.Map;

import com.brq.mobile.framework.core.BaseVo;

public class MercadoriaSimilar extends BaseVo {

	private static final long serialVersionUID = 3166566539069043451L;

	public Integer codigo;
	public String descricao;
	public Integer codigoPrincipal;

	@Override
	public Map<String, Object> toMap() {
		Map<String, Object> map = new HashMap<String, Object>();
		map.put("codigoSimilar", codigo);
		map.put("descricaoSimilar", descricao);
		return map;
	}

}
