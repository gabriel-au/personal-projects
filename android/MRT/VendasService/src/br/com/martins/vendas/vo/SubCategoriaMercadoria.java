package br.com.martins.vendas.vo;

import java.util.HashMap;
import java.util.Map;

import com.brq.mobile.framework.core.BaseVo;

public class SubCategoriaMercadoria extends BaseVo {
	
	private static final long serialVersionUID = 1L;

	public Integer				codigoGrupoMercadoria;

	public Integer				codigoCategoriaMercadoria;
	
	public Integer				codigoSubCategoriaMercadoria;

	public String				descricaoSubCategoriaMercadoria;

	@Override
	public Map<String, Object> toMap() {
		Map<String, Object> map = new HashMap<String, Object>();
		map.put("codigoGrupoMercadoria", codigoGrupoMercadoria);
		map.put("codigoCategoriaMercadoria", codigoCategoriaMercadoria);
		map.put("codigoSubCategoriaMercadoria", codigoSubCategoriaMercadoria);
		map.put("descricaoSubCategoriaMercadoria", descricaoSubCategoriaMercadoria);
		return map;
	}
}
