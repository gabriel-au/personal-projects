package br.com.martins.vendas.vo;

import java.util.HashMap;
import java.util.Map;

import com.brq.mobile.framework.core.BaseVo;

public class CidadePreco extends BaseVo {

	private static final long serialVersionUID = -159023984810182612L;

	/**
	 * Campo CODFILEPD
	 */
	public Integer codigoFilialExpedicao;

	/**
	 * Campo CODCIDPCO
	 */
	public Integer codigoCidadePreco;
	
	/**
	 * Campo NUMRLCCID
	 */
	public Integer numeroRelacionamentoCliente;
	

	@Override
	public Map<String, Object> toMap() {
		
		Map<String, Object> map = new HashMap<String, Object>();

		map.put("codigoFilialExpedicao", codigoFilialExpedicao);
		map.put("codigoCidadePreco", codigoCidadePreco);
		map.put("numeroRelacionamentoCliente", numeroRelacionamentoCliente);
	
		return map;		
	}
	

	
	
	
	
}
